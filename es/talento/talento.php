				<h3 id="section2">Convocatoria Internacional de Becas Predoctorales</h3>
				Convocatoria de 20 becas y contratos pre-doctorales anuales de cuatro años de duración para la realización de tesis co-dirigidas entre miembros de la UCM y UPM y/o Instituciones Agregadas al Campus.  La Orden de Bases de la mencionada convocatoria y la primera Convocatoria se han publicado en :<br/><br/>
				<ul>
					<li>UCM: <a href="http://www.ucm.es/info/ucmp/pags.php?tp=Ayudas%20de%20becas-contratos%20para%20la%20realizaci%F3n%20de%20tesis%20doctorales:&a=invest&d=0022941.php" target="_blank">Pulse para ver convocatoria.</a><br/></li>
					<li>UPM: <a href="http://www.upm.es/institucional/UPM/CanalUPM/Noticias/f7426351032fb210VgnVCM10000009c7648aRCRD" target="_blank">Pulse para ver convocatoria.</a><br/></li>
				</ul>
				<br/><br/>
				<b>Objetivo</b>: Realización de tesis doctorales <b>codirigidas</b> (UCM-UPM o miembro de una institución agregada al campus) en las áreas estratégicas del Campus de Excelencia Internacional (CEI) Campus de Moncloa: Cambio Global y Nuevas Energías, Materiales para el Futuro, Agroalimentación y Salud, Medicina Innovadora y Patrimonio. Los codirectores de las tesis deben pertenecer a los grupos adscritos al CEI de Moncloa.
				<br/><br/>
				<b>Solicitantes</b>: Según el artículo 3 de las bases reguladoras y el artículo 2 de la convocatoria, los solicitantes y proponentes deberán cumplir los siguientes requisitos:<br/><br/>
				<ul>
					<li>Poseer la nacionalidad española, ser nacional de un país miembro de la Unión Europea, o extranjero con residencia legal en España o tenerla en fase de tramitación.</li>
					<li>Los solicitantes deberán acreditar estar en posesión del título o superado los requisitos para acceder a los estudios Oficiales de Posgrado, conforme a alguna de las siguientes ordenaciones universitarias:</li>
					<br/>
					<p>Estudios Oficiales de Posgrado regulados por el Real Decreto 1393/2007, de 29 de
					octubre, por el que se establece la ordenación de las enseñanzas universitarias oficiales:</p>
					<br/>
					<ul>
						<li>1º Haber finalizado los estudios conducentes al título oficial español de Licenciado,
						Ingeniero o Arquitecto <b>con posterioridad al 1 de enero de 2006</b>.</li><br/>
						<li>2º Haber finalizado los estudios conducentes al título oficial español de Diplomado,
						Ingeniero Técnico y Arquitecto Técnico, y tener superados al menos 60 créditos de
						posgrado <b>con posteroridad al 1 de enero de 2005</b>.</li><br/>
						<li>3º No haber finalizado los estudios conducentes al título oficial español de Licenciado,
						Ingeniero o Arquitecto, y tener superados 300 créditos entre grado y posgrado, de los
						cuales al menos 60 correspondan a posgrado. El último requisito o asignatura que le haya
						dado acceso al master deberá ser <b>posterior al 1 de enero de 2007</b>.</li><br/>
						<li>En el caso de los solicitantes que hayan obtenido el título o realizado los estudios que den
						acceso al doctorado conforme a sistemas educativos extranjeros, se estará a lo dispuesto
						en el artículo 16 del Real Decreto 1393/2007, de 29 de octubre: podrán acceder los
						titulados conforme a sistemas educativos ajenos al Espacio Europeo de Educación Superior
						sin necesidad de la homologación de sus títulos, previa comprobación por la Universidad de
						que aquellos acreditan un nivel de formación equivalente a los correspondientes títulos
						universitarios oficiales españoles y que facultan en el país expedidor del título para el
						acceso a enseñanzas de postgrado.</li>
					</ul>
					<br/>
					<li>Estar preinscrito, matriculado o aceptado en el momento de inicio de la ayuda, en un programa de doctorado o de Máster orientado al doctorado del Campus de Moncloa, de la UCM o de la UPM, o bien en un programa interuniversitario en el que participe una o ambas universidades.</li>
					<li>No podrán solicitar estas ayudas quienes hayan disfrutado de otras de similares características durante un período de dos o más años ni quienes ya estén en posesión del título de doctor.</li>

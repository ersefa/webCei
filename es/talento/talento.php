				<h3 id="section3">Convocatoria Internacional de Contratos Posdoctorales</h3>
				Convocatoria de 15 contratos posdoctorales anuales de dos años de duración. Se ha publicado la Orden de bases de la mencionada convocatoria y la primera Convocatoria en:<br/><br/>
				<ul>
					<li>UCM: <a href="http://www.ucm.es/info/ucmp/pags.php?tp=Ayudas%20para%20la%20contrataci%F3n%20de%20j%F3venes%20doctores%20para%20la%20realizaci%F3n%20de%20trabajos%20de%20investigaci%F3n:&a=invest&d=0022932.php" target="_blank">Pulse para ver convocatoria.</a><br/></li>
					<li>UPM: <a href="http://www.upm.es/institucional/UPM/CanalUPM/Noticias/19056351032fb210VgnVCM10000009c7648aRCRD" target="_blank">Pulse para ver convocatoria.</a><br/></li>
				</ul>
				<br/><br/>
				El Campus de Moncloa en cumplimiento de su compromiso social con la formación de los Recursos Humanos, tiene la voluntad de estructurar una trayectoria investigadora que permita entroncar con los programas de I+D+i financiados por la Comunidad de Madrid, el Ministerio de Ciencia e Innovación, el Ministerio de Educación u otros programas de las Administraciones Públicas, contribuyendo a la consecución del Espacio Europeo de Investigación y en línea con las recomendaciones de la Comisión de las Comunidades Europeas relativas a la Carta Europea del Investigador y el Código de conducta para la contratación de investigadores (EEE/2005/251/CE). 
				<br/><br/>
				Las condiciones exigidas a los candidatos son las siguientes: 
				<br/><br/>
				<ul>
					<li>Haber obtenido el título de doctor durante los tres años anteriores a la publicación de la convocatoria.</li>
					<li>Acreditar la contribución de su actividad científica previa: tener, al menos, aceptada una publicación del Journal Citation Reports (JCR), y haber participado en, al menos, un proyecto de investigación competitivo en una universidad, empresa o industria.</li>
					<li>Ser menor de 35 años.</li>
					<li>Para la modalidad de incorporación: ser nacional de otros estados miembros o países asociados al Plan Marco de Investigación de la Unión Europea o terceros países (es decir no españoles y no residentes).</li>
					<li>Para la modalidad re-integración: Investigadores de los estados miembros o países asociados que hayan realizado investigación durante al menos tres años en terceros países (incluido el periodo de realización de la tesis doctoral).</li><br/>
				</ul>

				<h3 id="section1">Introducción</h3>
				<p>El Campus de Moncloa pretende definir un espacio de conectividad entre todas las instituciones agregadas, que permita explotar la riqueza que supone su complementariedad y aprovechar las fortalezas individuales de cada una, para, trabajando conjuntamente, transformar nuestra diversidad en energía, avanzando conectados al futuro.
				<br/><br/>
				El Plan Estratégico del Campus de Excelencia Internacional de  Moncloa preveía una política decidida de internacionalización y atracción de talento a través de diversas actuaciones, en especial el Programa Internacional de Captación de Talento (PICATA) centrado en la incorporación de estudiantes e investigadores brillantes de todo el mundo. La concentración de talento es la única forma de alcanzar la excelencia y en el Campus de Moncloa en lugar de limitarnos a la estrategia tradicional de captación de talento, en el queremos apostar por una estrategia de <b>concentración de talento</b>, una estrategia que engloba de modo inclusivo tres políticas diferenciadas:
				<br/><br/>
				<ul>
					<li>Cultivar / Incubar el talento</li>
					<li>Atraer / Captar el talento.</li>
					<li>Retener el talento.</li>
				</ul>
				<br/><br/>
				El objetivo final es actuar sobre todas las vertientes (cultivar, atraer, retener) del proceso de concentración de talento para alcanzar una masa crítica de estudiantes excepcionales, de profesores e investigadores de reconocido prestigio y de técnicos y gestores altamente cualificados. Por ello, dentro de la política de atracción y captación se ha procedido a definir los siguientes cinco subprogramas en los que UCM y UPM han procurado que las condiciones de disfrute de sus contratos sean, en lo posible, similares a las de los beneficiarios de las convocatorias de las administraciones públicas y evitar con ello la existencia de diferencias sustanciales que perjudiquen posteriormente a los beneficiarios de las mismas. 
				<br/><br/>

				<img src="../../images/talento.gif" hspace="0" vspace="0" align="right" />
				Las convocatorias de cada uno de los subprogramas podrán especificar líneas de investigación concretas en función del diseño, desarrollo y necesidades detectadas por los Planes Directores de los programas temáticos incluidos en el Plan Estratégico del Campus de Excelencia Internacional de Moncloa. El Campus de Moncloa definió cinco clústeres temáticos en los que aspira a ser referente científico y de captación y formación de talento: 
				<br/><br/><br/><br/>
				<ul>
					<li>Cambio Global y Nuevas Energías.<br/></li>
					<li>Materiales para el Futuro.<br/></li>
					<li>Agro-Alimentación y Salud.<br/></li>
					<li>Medicina Innovadora. <br/></li>
					<li>Patrimonio. </li>
				</ul>
				</p>

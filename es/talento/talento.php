				<h3 id="section4">Programa de Cátedras de Excelencia</h3>
				<p>Convocatoria de una plaza de profesor visitante anual en cada uno de los clústeres que será ocupada por un investigador del máximo prestigio en su área. Las estancias tendrán una duración continuada superior a tres meses. La publicación de la primera convocatoria está prevista en Marzo 2011.</p>
				<br/>

				<h3 id="section5">Convocatoria Internacional de Incorporación de Técnicos</h3>
				<p>Convocatoria de 5 plazas de Técnico Superior altamente cualificado de dos años de duración. Los técnicos se incorporarán a las Instalaciones Científico-Técnicas Singulares ya implantadas en el Campus y/o que se implantarán próximamente y las plataformas científico-tecnológicas que están en marcha y otras de interés estratégico del Campus que han sido solicitadas en la convocatoria INNOCAMPUS 2010. Publicación prevista de la convocatoria: Octubre 2011. Inicio de los contratos: Enero 2012.</p>
				<br/>

				<h3 id="section6">Convocatoria Internacional de Movilidad y Formación de Técnicos</h3>
				<p>Las actividades dirigidas a técnicos de alto grado de formación mediante estancias formativas de tecnólogos del CEI en laboratorios extranjeros y estancias en el CEI Moncloa de técnicos especialistas de otros laboratorios de reconocido prestigio internacional. El Subprograma de Fortalecimiento de la convocatoria 2010 del Programa Campus de Excelencia Internacional del Ministerio de Educación ha concedido 80.000 Euros para la realización de estas actividades.</p>

				<h3 id="section2">Introducción</h3>
				<p>La Ciudad Universitaria de Madrid fue concebida en 1927 como un campus integral, armonizando educación, ciencia y cultura. Ochenta y dos años después, el proyecto Campus de Moncloa quiere liderar la respuesta que el triángulo del conocimiento y el desarrollo sostenible plantean en el siglo XXI.</p>
				<br/>
				<p>Este proyecto, ambicioso y realista, se presenta conjuntamente por las Universidades Complutense y Politécnica de Madrid, sumando, también, otros organismos presentes en el Campus, como el CIEMAT, el CSIC o el INIA, con el objetivo de transformar el Campus de Moncloa en un <b>referente internacional</b> en investigación, formación e innovación.</p>
				<br/>
				<p>El proyecto se estructura sobre un <b>acuerdo de colaboración</b> entre las instituciones agregadas, guiado por los ejes estratégicos de alcanzar la excelencia científica y la internacionalización, garantizar la conectividad y la agregación y hacer del campus un sistema sostenible, propiciando el empleo de los estudiantes en el mismo y contribuyendo a la innovación y al desarrollo. Perseguimos un campus participativo y plural, inspirado en la potencia transformadora de la diversidad, el intercambio y el diálogo; un campus eficiente, con una gobernanza transparente, abierta a todas las personas e instituciones que lo integran y a la interacción con el tejido social, económico y cultural.</p>
				<br/>
				<p>El Campus apuesta por la <b>especialización en cinco clústeres</b> temáticos en los que aspira a la excelencia científica y docente: Cambio Global y Nuevas Energías, Materiales para el Futuro, Agro-Alimentación y Salud, Medicina Innovadora y Patrimonio. En todos ellos, la unión de fortalezas proporciona configuraciones únicas por su carácter innovador e interdisciplinar, altamente competitivas en el nivel europeo y capaces de producir un avance decisivo en la transferencia de conocimiento.</p>
				<br/>
				<p>La <b>conectividad</b> es el elemento clave para lograr la agregación. El campus contempla la interconexión de los investigadores e instituciones de cada clúster por medio de una estructura de coordinación propia, así como la interdependencia de los mismos por medio del sistema de general gobernanza.
				</p><br/>
				<p>Integrado en el área urbana de Madrid, el Campus y las personas que en él conviven y la Ciudad, serán los principales destinatarios de esta apuesta. Moncloa se define como un <b>campus sostenible, saludable y socialmente responsable</b>. Su proyecto incluye actuaciones para la recuperación de su patrimonio arquitectónico, paisajístico, cultural, y su utilización didáctica, y para disfrute de los ciudadanos. Pretende la optimización energética y la promoción del transporte sostenible, la mejora de la salud y la convivencia. Moncloa será, además, un Campus plenamente accesible a las personas con discapacidad, comprometido con la equidad de género, la formación del talento, la igualdad de oportunidades y la cooperación al desarrollo. </p><br/>
				<p>Campus de Moncloa integra instituciones diversas; se apoya en la interdisciplinariedad y la transversalidad; busca atraer estudiantes e investigadores de todo el mundo, con su pluralidad de lenguas y culturas, y hace de la diversidad medioambiental uno de sus objetivos científicos. En definitiva, cree en la agregación de <b>la Diversidad como Energía</b> innovadora para la excelencia y el desarrollo sostenible.</p><br/>
